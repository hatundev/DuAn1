    private LoginRequest readForm() {
        LoginRequest login = null;
        String username = txtUsername.getText();
        if (username.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "Vui lòng nhập user và password");
//            Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER,"Chưa nhập username");
            lbThongBao.setText("Chưa nhập username!");
            txtUsername.requestFocus();
            return login;
        }
        String password = txtPassword.getText();
        if (password.isEmpty()) {
            lbThongBao.setText("Chưa nhập password!");
            txtPassword.requestFocus();
            return login;
        }
        login = new LoginRequest(username, password);
        return login;
    }

