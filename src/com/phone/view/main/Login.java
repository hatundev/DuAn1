    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        // TODO add your handling code here:
        LoginRequest loginRequest = readForm();
        if (loginRequest != null) {
            int result = taiKhoanServiceImpl.login(loginRequest);
            if (result == 0) {
                lbThongBao.setText("Username không tồn tại!");
                txtUsername.requestFocus();
            } else if (result == -1) {
                lbThongBao.setText("Sai mật khẩu!");
                txtPassword.requestFocus();
            } else {
                TaiKhoanResponse tk = taiKhoanServiceImpl.findByMaNhanVien(loginRequest.getUsername());
                this.dispose();
                Main main = new Main();
                main.setAccount(tk);
                main.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

